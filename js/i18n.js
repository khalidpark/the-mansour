// RTL Languages
const RTL_LANGS = ['ar', 'he', 'fa'];

// Language display names
const LANG_NAMES = {
  ko: '한국어',
  en: 'English',
  ja: '日本語',
  zh: '中文',
  ar: 'العربية',
};

// Current language
let currentLang = 'ko';

function t(key) {
  const lang = TRANSLATIONS[currentLang] || TRANSLATIONS['en'];
  const fallback = TRANSLATIONS['en'];
  return lang[key] || fallback[key] || key;
}

function setLanguage(lang) {
  currentLang = lang;
  const isRTL = RTL_LANGS.includes(lang);

  document.documentElement.setAttribute('lang', lang);
  document.documentElement.setAttribute('dir', isRTL ? 'rtl' : 'ltr');

  // Update all translatable elements
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    const translated = t(key);
    if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
      el.placeholder = translated;
    } else {
      el.innerHTML = translated;
    }
  });

  // Update lang switcher display
  const langBtn = document.querySelector('.lang-switcher__current');
  if (langBtn) {
    langBtn.textContent = lang.toUpperCase();
  }

  // Mark active language
  document.querySelectorAll('.lang-switcher__option').forEach(opt => {
    opt.classList.toggle('active', opt.dataset.lang === lang);
  });

  // Store preference
  localStorage.setItem('themansour-lang', lang);
}

function initI18n() {
  const saved = localStorage.getItem('themansour-lang');
  const browserLang = navigator.language.slice(0, 2);
  const defaultLang = saved || (TRANSLATIONS[browserLang] ? browserLang : 'ko');
  setLanguage(defaultLang);
}
